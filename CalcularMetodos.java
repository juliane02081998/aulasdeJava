package metodos;

public class CalcularMetodos {
	int somar(int a, int b) {
		return a+b;
	}
	int somar (int a, int b, int c) {
		return a+b+c;
	}
	int multiplicar(int a, int b, int c){
		return a*b*c;
	}
	int subtrair(int a, int b, int c) {
		return a-b-c;
	}
	int dividir(int a, int b, int c) {
		return a/b/c;
		
	}
}
